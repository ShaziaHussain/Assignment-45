// Array of favorite pizzas
const pizzas = ["Pepperoni", "Margherita", "BBQ Chicken"];

// Loop through the array to print the name of each pizza
for (let i = 0; i < pizzas.length; i++) {
    // Print a statement for each pizza
    console.log(`I like ${pizzas[i]} pizza.`);
}

// Print a sentence expressing overall love for pizza
console.log("I really love pizza!");
